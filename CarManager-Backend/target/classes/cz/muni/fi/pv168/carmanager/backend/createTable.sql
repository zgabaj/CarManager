CREATE TABLE "CAR"(
    "ID" BIGINT NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
    "BRAND" VARCHAR(30),
    "LICENCEPLATE" VARCHAR(10)
);

CREATE TABLE "LEASE"(
    "ID" BIGINT NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY, 
    "CUSTOMERFULLNAME" VARCHAR(50),
    "STARTDATE" DATE,
    "ENDDATE" DATE,
    "CARID" BIGINT
);